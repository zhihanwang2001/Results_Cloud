export const agentsById = agents.reduce((map, agent) => {
  map[agent.id] = agent
  return map
}, {})

export function groupAgentsBySuite() {
  return agents.reduce((groups, agent) => {
    if (!groups[agent.suite]) {
      groups[agent.suite] = {
        suite: agent.suite,
        description: agent.suiteDescription,
        agents: [],
      }
    }
    groups[agent.suite].agents.push(agent)
    return groups
  }, {})
}

export function toSuiteAnchor(label) {
  return `suite-${label.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`
    .replace(/-+/g, '-')
    .replace(/-$/, '')
}

